    private void btnTicketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTicketActionPerformed
        // TODO add your handling code here:
        
        if(customer == null || flight == null) {
            JOptionPane.showMessageDialog(null, "Please select", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Ticket buyTicket = this.travelAgency.buyTicket(this.customer, this.flight);
        
        if(buyTicket == null) {
            JOptionPane.showMessageDialog(null, "Ticket is null, fail to buy");
            return;
        }
        
        ResultTicketJPanel panel = new ResultTicketJPanel(travelAgency, buyTicket, userProcessContainer);
        
        userProcessContainer.add("TicketResultJPanel", panel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnTicketActionPerformed

    private void btnSeatTicketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeatTicketActionPerformed
        // TODO add your handling code here:
        
        String seat = txtSeat.getText();
        
        if(customer == null || flight == null) {
            JOptionPane.showMessageDialog(null, "Please select", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Ticket buyTicket = this.travelAgency.buyTicket(this.customer, this.flight, seat);
        
        if(buyTicket != null) {
            
            ResultTicketJPanel panel = new ResultTicketJPanel(travelAgency, buyTicket, userProcessContainer);
        
            userProcessContainer.add("TicketResultJPanel", panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
            
        } else {
            
            JOptionPane.showMessageDialog(null, "Buy Ticket Fail!", "Warning", JOptionPane.WARNING_MESSAGE);
            
        }
    }//GEN-LAST:event_btnSeatTicketActionPerformed



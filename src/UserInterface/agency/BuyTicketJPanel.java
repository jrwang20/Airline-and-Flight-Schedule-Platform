    private void btnCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCustomerActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = tblCustomer.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            this.customer = (Customer) tblCustomer.getValueAt(selectedRow, 0);
            
            List<Flight> customerFlightList = this.travelAgency.getOffice().getCustomerFlightList(customer);
            
            populateCustomizedTable(customerFlightList);
            
        }
        
        
    }//GEN-LAST:event_btnCustomerActionPerformed

